hvad_er_kata = input("Er kata leid/glod eða reid?")

if hvad_er_kata == "leid":
    print("Kata er leið því að pabbi hennar er fullur")
elif hvad_er_kata == "glod":
    print("Kata er glöð því að hún er í nikkara")
elif hvad_er_kata == "reid":
    print("Kata er reið því að Vala vildi ekki fara á B5")
else :
    print("Kata getur ekki verið eitthvað annað en leið, reið eða glöð!!")
    

